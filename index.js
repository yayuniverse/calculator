// Operator functions
function add(num1, num2) {
  return num1 + num2;
}
function subtract(num1, num2) {
  return num1 - num2;
}
function multiply(num1, num2) {
  return num1 * num2;
}
function divide(num1, num2) {
  return num1 / num2;
}

// Operate function
function operate(num1, num2, operator) {
  return operator(num1, num2)
}

const num1 = null;
const num2 = null;
const operator = null;
